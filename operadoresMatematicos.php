//Operadores Matematicos


$num1 = 25 ;
$num2 = 10;

// Suma : Se realiza con el operador  +

echo $num1 + $num2 ;  // devuelve 35
echo "<br>";



//Resta :  Se realiza con el operador  -

echo $num1 - $num2; // devuelve 15
echo "<br>";



//Multiplicacion:  Se realiza con el operador  *

echo $num1*$num2; // devuelve 250
echo "<br>";



//Division :  Se realiza con el operador  /

echo $num1/$num2; // devuelve 2.5
echo "<br>";



//Modulo o resto :  Se realiza con el operador  %

echo $num1%$num2; // devuelve 5
echo "<br>";

?>